function AllItems(Collections) {

    const ItemsList_Div = document.getElementById('Items_div');
    var Items_img_index = 0;
    var ItemsDiv;
    var types;
    
    function CreateProductDiv(item, index) {
        return `
            <div class="products-div" onclick="viewProduct('${item.name}')">
                <img src="../Assets/${item.type}/item${index}_1.png">
                <div class="product-name">
                    <h3>${item.name}</h3>
                    <span>₱${item.price}</span>
                </div>
            </div>
        `;
    }
    if (Collections === 0) {
        types = ItemsList.filter(cate => cate.type === "Jackets");
    } else if (Collections === 1) {
        types = ItemsList.filter(cate => cate.type === "T_Shirts");
    } else if (Collections === 2) {
        types = ItemsList.filter(cate => cate.type === "Shorts");
    } else if (Collections === 3) {
        types = ItemsList.filter(cate => cate.type === "Pants");
    } else {
        types = ItemsList;
    }

    types.forEach(item => {
        if (Items_img_index === 75 || Items_img_index === 50 || Items_img_index === 25) {
            Items_img_index = 0;
        }

        ItemsDiv = document.createElement('div');
        ItemsDiv.innerHTML = CreateProductDiv(item, Items_img_index);
        ItemsList_Div.appendChild(ItemsDiv);
        Items_img_index++;
    });
    
}

function viewProduct(Product_name) {
    window.location.href = "Items.html?product=" + encodeURIComponent(Product_name); 
}

function Create_Other_div(){
    const Items_Others = document.getElementById('Items_Others');
    var Items_img_index = 0;
    var ItemsDiv;
    var types = ["Jackets","T_Shirts","Shorts","Pants"] 

    function CreateOtherDiv(Types,Items) {
        var types = ItemsList.filter(cate => cate.type === Types)
        return `
           <div class="products-div" onclick="viewProduct('${types[Items].name}')">
                <img src="../../Assets/${Types}/item${Items}_1.png">
                <div class="product-name">
                    <h3>${types[Items].name}</h3>
                    <span>₱${types[Items].price}</span>
                </div>
            </div>
        `;  
    }
    
    for (var i = 0; i < 4; i++){
        var items = Math.floor(Math.random() * 18);
        ItemsDiv = document.createElement('div');
        ItemsDiv.innerHTML = CreateOtherDiv(types[i],items);
        Items_Others.appendChild(ItemsDiv);
        Items_img_index++;
    }
}

